package com.findme.models;

public class ValidateDate {
    private int quantityOfFriendsFrom;
    private int quantityOfFriendsTo;
    private int quantityOfOutcomeRequests;
    private int daysFriends;

    public int getQuantityOfFriendsFrom() {
        return quantityOfFriendsFrom;
    }

    public int getQuantityOfFriendsTo() {
        return quantityOfFriendsTo;
    }

    public int getQuantityOfOutcomeRequests() {
        return quantityOfOutcomeRequests;
    }

    public int getDaysFriends() {
        return daysFriends;
    }

    public void setQuantityOfFriendsFrom(int quantityOfFriendsFrom) {
        this.quantityOfFriendsFrom = quantityOfFriendsFrom;
    }

    public void setQuantityOfFriendsTo(int quantityOfFriendsTo) {
        this.quantityOfFriendsTo = quantityOfFriendsTo;
    }

    public void setQuantityOfOutcomeRequests(int quantityOfOutcomeRequests) {
        this.quantityOfOutcomeRequests = quantityOfOutcomeRequests;
    }

    public void setDaysFriends(int daysFriends) {
        this.daysFriends = daysFriends;
    }
}
